package com.java.operator;

public class Relational {

	public static void main(String[] args) {
		int a=50;
		System.out.println(a);
		int b=100;
		System.out.println(b);
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a<=b);
		System.out.println(a>=b);
		System.out.println(a==b);
		System.out.println(a!=b);

	}

}
